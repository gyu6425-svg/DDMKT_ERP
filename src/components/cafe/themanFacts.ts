/** 하단 태그 10개 — 지역+서비스에서 만든다. 예: '과천 건물보안' → 과천건물보안, 과천경비업체, … */
export function themanTags(region: string, service: string): string[] {
    const r = (region || '').replace(/\s/g, '');
    const s = (service || '').replace(/\s/g, '');
    return [`${r}${s}`, `${r}경비업체`, `${r}보안업체`, `${r}건물관리`,
        s, '경비업체', '보안업체', '시설경비', '건물관리', '더맨시스템']
        .filter(Boolean).slice(0, 10);
}
