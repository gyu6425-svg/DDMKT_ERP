// 더맨시스템 정보형(info-guide) 원고 — 프롬프트 + 검증.
//   배포용 functions/api/generate-cafe.ts 와 로컬 dev scripts/openai-card-image-api.mjs 가 '둘 다' 이 파일을 쓴다.
//   (두 곳에 같은 프롬프트를 복붙했다가 조용히 갈라진 전례가 있어, 신규 코드는 처음부터 한 벌만 둔다.
//    기존 후기형 프롬프트는 건드리지 않는다 — 누수탐지 탭들의 동작을 바꾸지 않기 위해.)
//
// 설계 근거: 상위노출 잘 되는 동종 업체 글 5편을 분석해 '반복되는 공식'만 규칙으로 옮겼다.
//   문장·문구는 가져오지 않는다(표절 회피). 표는 발행기가 지원하지 않아 아예 쓰지 않는다.

// 발행기(crawler/cafe_pub/publish_cafe.py)와 '동일한' 정규식이어야 한다.
//   여기서 통과한 원고가 저기서 다르게 파싱되면 사진이 통째로 유실된다.
export const IMG_MARK = /^\s*[「[]?\s*사진\s*(\d+)\s*[」\]]?\s*$/;
export const SUB_MARK = /^\s*부제목\s*[:：]\s*(.+?)\s*$/;

// 본문 순수 글자수 — 사진 마커와 줄바꿈은 세지 않는다(발행기 기준과 일치).
export function bodyLen(body) {
    return String(body ?? '').replace(/「사진\s*\d+」/g, '').replace(/[\r\n]/g, '').length;
}

const MIN_LEN = 2300;   // 이 아래면 재생성. 기존 후기형(2000)보다 높게 잡되,
const MAX_LEN = 2800;   //   구조 제약이 많아 3,000자 이상은 현실적으로 잘 안 나온다.

export const INFO_GUIDE_LEN = { max: MAX_LEN, min: MIN_LEN };

