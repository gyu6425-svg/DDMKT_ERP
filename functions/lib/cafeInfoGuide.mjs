/** 발행 전 형식 점검. 고치지는 않는다 — 무엇이 틀렸는지만 돌려준다. */
export function validateInfoBody(body, count) {
    const problems = [];
    const lines = String(body ?? '').split(/\r?\n/);
    const n = Math.max(1, Math.min(9, Number(count) || 9));

    // ── 사진 마커: 개수·번호·독립줄 ──
    const nums = [];
    for (const ln of lines) {
        const m = IMG_MARK.exec(ln);
        if (m) nums.push(Number(m[1]));
    }
    if (nums.length !== n) problems.push(`사진 마커 ${nums.length}개(기대 ${n}개)`);
    for (let i = 0; i < n; i += 1) {
        if (nums.filter((x) => x === i + 1).length !== 1) problems.push(`「사진 ${i + 1}」이 정확히 1번 나오지 않음`);
    }
    if (nums.some((x) => x > n)) problems.push(`범위를 넘는 사진 번호(발행 시 조용히 사라짐)`);
    // 줄 안에 섞여 들어간 마커 — 발행기가 못 알아보고 글자로 노출된다.
    for (const ln of lines) {
        if (!IMG_MARK.test(ln) && /[「[]\s*사진\s*\d+\s*[」\]]/.test(ln)) {
            problems.push(`문장 안에 섞인 사진 마커: "${ln.trim().slice(0, 40)}"`);
        }
    }

    // ── 부제목: 중복 금지 + 본문 줄과의 충돌 금지 ──
    //   발행기는 innerText 완전일치로 문단을 찾는다. 부제목과 똑같은 평문 줄이 앞에 있으면
    //   그 평문이 인용구로 바뀌고 진짜 소제목은 평문으로 남는다.
    const subs = [];
    const plain = [];
    for (const ln of lines) {
        const m = SUB_MARK.exec(ln);
        if (m) subs.push(m[1].replace(/^(?:부제목\s*[:：]\s*)+/, '').trim());
        else if (ln.trim() && !IMG_MARK.test(ln)) plain.push(ln.trim());
    }
    const seen = new Set();
    for (const s of subs) {
        const key = s.replace(/\s+/g, ' ');
        if (seen.has(key)) problems.push(`부제목 중복: "${s}"`);
        seen.add(key);
        if (plain.some((t) => t.replace(/\s+/g, ' ') === key)) problems.push(`부제목이 본문 줄과 동일: "${s}"`);
        if (s.length < 8 || s.length > 34) problems.push(`부제목 길이 이탈(${s.length}자): "${s}"`);
    }
    // 개수를 정확히 맞추라고 판정하면 재시도만 계속 태우고(실측 5/6 실패) 발행 품질은 달라지지 않는다.
    //   발행기는 있는 만큼 인용구로 바꿀 뿐이다. 너무 적어 구획이 사라지는 경우만 잡는다.
    const wantSub = Math.max(3, Math.min(6, n - 3));
    const loSub = Math.max(3, wantSub - 2);
    const hiSub = wantSub + 2;
    if (subs.length < loSub || subs.length > hiSub) {
        problems.push(`부제목 ${subs.length}개(${loSub}~${hiSub}개여야 함)`);
    }

    // ── 발행기가 해석하지 못하고 글자 그대로 나가는 것들 ──
    const banned = [
        [/\*\*/, '마크다운 굵게(**)'],
        [/^#{1,6}\s/m, '마크다운 제목(#)'],
        [/^\s*[-*]\s+/m, '목록 기호(-, *)'],
        [/\|/, '표 문자(|)'],
        [/^\s*>/m, '인용 기호(>)'],
        [/https?:\/\//, 'URL'],
        [/#[^\s#]+/, '해시태그'],
        [/[\u{1F300}-\u{1FAFF}\u{2600}-\u{27BF}]/u, '이모지'],
        // "문제가"의 '제가', "마무리"의 '우리'처럼 다른 낱말 속에 묻힌 경우를 세면 안 된다 → 앞을 한글이 아닌 것으로 제한.
        [/(^|[^가-힣])(저희|우리는|우리가|제가|저는)/m, '1인칭 표현'],
        [/한다\.|된다\.|이다\./, '해라체(-다) 종결 — 합니다체로 통일해야 함'],
        [/(적시한다|제시한다|나열한다|설명한다|서술한다|묘사한다|적는다)/, '내용 대신 "무엇을 쓸지"를 서술함'],
        [/\[확인된 사실\]|\[정보형|\[반드시/, '프롬프트 지시문이 본문에 유출됨'],
        [/0\d{1,2}[-\s.]?\d{3,4}[-\s.]?\d{4}/, '전화번호'],
    ];
    for (const [re, label] of banned) if (re.test(body || '')) problems.push(`금칙: ${label}`);

    // FAQ 는 최소 1쌍 ~ 최대 3쌍(사용자 요청 2026-07-20). 3쌍 강제는 통과율이 너무 낮았다(8회 중 3회).
    //   질문만 있고 답이 없는(또는 그 반대) 짝 안 맞는 상태도 막는다.
    const nQ = (String(body || '').match(/^\s*Q\.\s*\S/gm) || []).length;
    const nA = (String(body || '').match(/^\s*A\.\s*\S/gm) || []).length;
    if (nQ !== nA) problems.push(`FAQ 짝 안 맞음(Q${nQ}/A${nA})`);
    else if (nQ > 3) problems.push(`FAQ ${nQ}쌍(최대 3쌍)`);
    // 0쌍은 허용 — 억지로 만든 질문보다 없는 편이 낫다(사용자 요청 2026-07-20).

    // 도입부는 첫 「사진 1」 앞에 문단이 2개여야 한다(한 덩어리로 붙지 않게).
    const head = String(body || '').split(/^\s*[「[]?\s*사진\s*1\s*[」\]]?\s*$/m)[0] || '';
    const nIntro = head.split(/\n\s*\n/).map((s) => s.trim()).filter(Boolean).length;
    if (nIntro !== 2) problems.push(`도입부 문단 ${nIntro}개(2개여야 함)`);

    const len = bodyLen(body);
    if (len < MIN_LEN) problems.push(`분량 부족 ${len}자(최소 ${MIN_LEN})`);

    return { len, markers: nums.length, ok: problems.length === 0, problems, subtitles: subs.length };
}
